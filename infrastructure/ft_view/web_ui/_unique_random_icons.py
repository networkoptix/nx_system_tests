from binascii import crc32


def unique_random_icon(value):
    """Generate Unicode icon to represent same and different values.

    May be used for hashes or other values that has no meaning and should only
    be compared with each other.
    """
    return _dingbats[crc32(str(value).encode()) % len(_dingbats)]


